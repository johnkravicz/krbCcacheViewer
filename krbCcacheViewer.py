    ticket_type = guess_ticket_type(cred)
    client_str  = principal_to_str(cred["client"])
    server_str  = principal_to_str(cred["server"])

    banner(f"CREDENTIAL #{idx + 1}  –  {ticket_type}")

    # Ticket type coloring
    if "TGT" in ticket_type:
        pentest_flag("Ticket type", ticket_type,
                     "TGT – can be used to request any ST / pass-the-ticket")
    else:
        info_flag("Ticket type", ticket_type)

    section("Principals")
    field("Client", client_str, CYAN)
    field("Server", server_str, CYAN)

    # Detect cross-realm TGT
    if "inter-realm" in ticket_type or "referral" in ticket_type:
        pentest_flag("Cross-realm TGT", server_str,
                     "Inter-realm ticket – possible forest/domain trust traversal")

    section("Session key")
    display_etype(cred["keyblock"]["etype"])
    field("Key length", f"{len(cred['keyblock']['key'])} bytes")

    display_times(cred)

    section("Ticket flags")
    display_flags(cred["tick_flags"])

    # Addresses
    if cred["addresses"]:
        section("Host addresses (IP restriction)")
        for (atype, adata) in cred["addresses"]:
            if atype == 2 and len(adata) == 4:  # IPv4
                ip = socket.inet_ntoa(adata)
                info_flag("IPv4 restriction", ip,
                          "Ticket is IP-restricted – may not work from other hosts")
            elif atype == 24 and len(adata) == 16:  # IPv6
                info_flag("IPv6 restriction", adata.hex())
            else:
                field(f"Address type {atype}", adata.hex())
    else:
        info_flag("No address restriction", "Ticket usable from any IP",
                  "Pass-the-ticket attack is viable from any host")

    # Ticket raw size
    section("Raw ticket blob")
    field("Ticket blob size", f"{len(cred['ticket'])} bytes")
    if cred["ticket"]:
        field("First bytes (hex)", cred["ticket"][:16].hex() + "...", DIM)

    # PAC extraction – try ccache authdata first, then scan ticket blob
    pac_data = find_pac_in_authdata(cred["authdata"])
    pac_source = "ccache authdata"
    if pac_data is None:
        pac_data = find_pac_in_ticket_asn1(cred["ticket"])
        pac_source = "ticket ASN.1 heuristic scan"

    if pac_data:
        print(f"\n  {GREEN}[+] PAC found ({pac_source}, {len(pac_data)} bytes){RESET}")
        try:
            pac_buffers = parse_pac(pac_data)
            display_pac(pac_buffers)
        except Exception as e:
            print(f"  {RED}[-] PAC parse failed: {e}{RESET}")
    else:
        section("PAC")
        field("Status", "PAC not found in this credential entry", YELLOW)
        field("Note", "PAC is encrypted inside the ticket and cannot be read "
              "without the KDC/service private key", DIM)

