class Buffer:
    """Simple cursor-based binary reader."""

    def __init__(self, data: bytes):
        self.data = data
        self.pos  = 0

    def remaining(self):
        return len(self.data) - self.pos

    def read(self, n: int) -> bytes:
        if self.pos + n > len(self.data):
            raise ValueError(f"Buffer underflow: need {n} bytes at offset {self.pos}, "
                             f"only {self.remaining()} left")
        chunk = self.data[self.pos:self.pos + n]
        self.pos += n
        return chunk

    def read_u8(self)  -> int: return struct.unpack(">B", self.read(1))[0]
    def read_u16_be(self) -> int: return struct.unpack(">H", self.read(2))[0]
    def read_u32_be(self) -> int: return struct.unpack(">I", self.read(4))[0]
    def read_u16_le(self) -> int: return struct.unpack("<H", self.read(2))[0]
    def read_u32_le(self) -> int: return struct.unpack("<I", self.read(4))[0]
    def read_u64_le(self) -> int: return struct.unpack("<Q", self.read(8))[0]
    def read_i32_le(self) -> int: return struct.unpack("<i", self.read(4))[0]

    def peek(self, n: int) -> bytes:
        return self.data[self.pos:self.pos + n]

    def seek(self, pos: int):
        self.pos = pos

    def at(self, pos: int) -> "Buffer":
        """Return a new Buffer view starting at absolute offset pos."""
        return Buffer(self.data[pos:])

# ─────────────────────────────────────────────────────────────────────────────
# ccache format parser  (MIT KRB5 ccache v3/v4)
# ─────────────────────────────────────────────────────────────────────────────

def read_ccache_counted_octet(buf: Buffer) -> bytes:
    length = buf.read_u16_be()
    return buf.read(length)

def read_ccache_principal(buf: Buffer) -> dict:
    """
    Parse a ccache Principal structure.

    Format (MIT ccache spec + impacket):
      [u32_be] name_type
      [u32_be] num_components
      [u32_be] realm_length     ← CountedOctetString uses 4-byte length (u32_be)
      [realm_length bytes] realm
      for each component:
        [u32_be] component_length
        [component_length bytes] data

    Common mistake: using u16_be for lengths; the spec mandates u32_be.
    """
    name_type   = buf.read_u32_be()
    num_comp    = buf.read_u32_be()
    realm_len   = buf.read_u32_be()          # ← was u16_be (bug fix)
    realm       = buf.read(realm_len).decode("utf-8", errors="replace")
    components  = []
    for _ in range(num_comp):
        comp_len = buf.read_u32_be()         # ← was u16_be (bug fix)
        components.append(buf.read(comp_len).decode("utf-8", errors="replace"))
    return {"name_type": name_type, "realm": realm, "components": components}

def principal_to_str(p: dict) -> str:
    comps = p.get("components", [])
    realm = p.get("realm", "")
    if comps:
        return "/".join(comps) + "@" + realm
    elif realm:
        return "@" + realm  # degenerate principal (realm only)
    return "(empty principal)"

def read_keyblock(buf: Buffer, ccache_version: int = 4) -> dict:
    """
    Parse a KeyBlock structure.

    KeyBlockV4 (ccache version 4 – impacket default):
      [u16_be] keytype
      [u16_be] etype       ← second field, often mirrors keytype
      [u16_be] keylen
      [keylen bytes] keyvalue

    KeyBlockV3 (ccache version 3):
      Same layout as V4. The "etype repeated twice" in impacket's PR #1106
      comment refers to V3 ccache files generated by old MIT krb5 where
      the etype field is a redundant copy of keytype — same binary layout.

    Both versions are therefore identical in byte layout (3 × u16 + data).
    We read them the same way and expose both keytype and etype separately.
    """
    keytype = buf.read_u16_be()
    etype   = buf.read_u16_be()   # may mirror keytype in v3; actual etype in v4
    keylen  = buf.read_u16_be()
    key     = buf.read(keylen)
    # Use keytype as the canonical etype (they are the same value in practice)
    return {"etype": keytype, "etype2": etype, "key": key}

def kerberos_time(t: int) -> str:
    """Convert a POSIX timestamp (32-bit) to a human-readable string."""
    if t == 0:
        return "0 (not set)"
    try:
        return datetime.datetime.fromtimestamp(t, tz=datetime.timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    except Exception:
        return str(t)

def decode_ticket_flags(flags: int) -> list:
    """Return list of (flag_name, is_notable) for set bits."""
    result = []
    for bit, (name, notable) in TICKET_FLAGS.items():
        # Flags are stored big-endian in the 32-bit int, bit 0 = MSB
        if flags & (1 << (31 - bit)):
            result.append((name, notable))
    return result

def read_credential(buf: Buffer, ccache_version: int = 4) -> dict:
    client    = read_ccache_principal(buf)
    server    = read_ccache_principal(buf)
    keyblock  = read_keyblock(buf, ccache_version)
    auth_time = buf.read_u32_be()
    start_time= buf.read_u32_be()
    end_time  = buf.read_u32_be()
    renew_till= buf.read_u32_be()
    is_skey   = buf.read_u8()
    tick_flags= buf.read_u32_be()
    # addresses
    num_addr  = buf.read_u32_be()
    addresses = []
    for _ in range(num_addr):
        atype = buf.read_u16_be()
        alen  = buf.read_u16_be()
        adata = buf.read(alen)
        addresses.append((atype, adata))
    # authdata
    num_ad    = buf.read_u32_be()
    authdata  = []
    for _ in range(num_ad):
        adtype = buf.read_u16_be()
        adlen  = buf.read_u16_be()
        addata = buf.read(adlen)
        authdata.append((adtype, addata))
    # ticket blob (CountedOctetString: u32_be length)
    ticket_len = buf.read_u32_be()
    ticket     = buf.read(ticket_len)
    # second_ticket (usually empty)
    sec_ticket_len = buf.read_u32_be()
    sec_ticket = buf.read(sec_ticket_len)

    return {
        "client":     client,
        "server":     server,
        "keyblock":   keyblock,
        "auth_time":  auth_time,
        "start_time": start_time,
        "end_time":   end_time,
        "renew_till": renew_till,
        "is_skey":    is_skey,
        "tick_flags": tick_flags,
        "addresses":  addresses,
        "authdata":   authdata,
        "ticket":     ticket,
        "sec_ticket": sec_ticket,
    }

def parse_ccache(data: bytes) -> dict:
    buf = Buffer(data)
    file_format_version = buf.read_u16_be()
    if file_format_version not in (0x0503, 0x0504, 0x0502):
        print(f"{YELLOW}[*] Unexpected ccache version: 0x{file_format_version:04x} "
              f"(continuing anyway){RESET}")

    # Header tags (version 4 only)
    tags = []
    if file_format_version == 0x0504:
        header_len = buf.read_u16_be()
        hdr_data   = buf.read(header_len)
        hb = Buffer(hdr_data)
        while hb.remaining() >= 4:
            tag  = hb.read_u16_be()
            tlen = hb.read_u16_be()
            tval = hb.read(tlen)
            tags.append((tag, tval))

    # Default principal
    default_principal = read_ccache_principal(buf)

    # Credentials
    credentials = []
    cred_idx = 0
    while buf.remaining() > 0:
        pos_before = buf.pos
        try:
            cred = read_credential(buf, file_format_version & 0xFF)
            credentials.append(cred)
            cred_idx += 1
        except ValueError as e:
            # Provide actionable debug info if parsing fails mid-stream
            print(f"{YELLOW}[*] Credential #{cred_idx + 1} parse error at offset "
                  f"{buf.pos} (started at {pos_before}): {e}{RESET}")
            print(f"{DIM}    Remaining bytes: {buf.remaining()} | "
                  f"Next 16 bytes: {buf.data[buf.pos:buf.pos+16].hex()}{RESET}")
            break

    return {
        "version":           file_format_version,
        "tags":              tags,
        "default_principal": default_principal,
        "credentials":       credentials,
    }

