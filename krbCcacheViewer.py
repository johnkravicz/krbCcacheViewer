# Ticket type heuristics
# ─────────────────────────────────────────────────────────────────────────────

def guess_ticket_type(cred: dict) -> str:
    """
    Determine whether a credential is a TGT, a Service Ticket, or a Referral.
    Heuristics based on server principal naming conventions.
    """
    server    = cred["server"]
    comps     = server["components"]
    realm     = server["realm"]

    if len(comps) >= 1 and comps[0].upper() == "KRBTGT":
        if len(comps) == 2 and comps[1].upper() != realm.upper():
            return "TGT (inter-realm / referral)"
        return "TGT"
    if len(comps) >= 2 and comps[0].upper() in ("CIFS", "HTTP", "HOST", "LDAP",
                                                   "MSSQLSvc", "TERMSRV", "WSMAN",
                                                   "RestrictedKrbHost", "GC"):
        return "Service Ticket (ST)"
    if len(comps) == 1:
        return "Service Ticket (ST) – single-component SPN"
    return "Service Ticket (ST)"

# ─────────────────────────────────────────────────────────────────────────────
# PAC parser
# ─────────────────────────────────────────────────────────────────────────────
#
# The PAC is embedded as KRB5 AuthData type 128 (AD-WIN2K-PAC).  The ticket
# blob itself is ASN.1 DER-encoded; rather than pull in a full ASN.1 parser
# we use a simple pattern search to locate the AuthData element, then parse
# the PAC binary directly.
#

AD_WIN2K_PAC = 128  # AuthData type for the PAC

def find_pac_in_authdata(authdata: list) -> bytes | None:
    """Search top-level ccache authdata entries for the PAC."""
    for (adtype, addata) in authdata:
        if adtype == AD_WIN2K_PAC:
            return addata
    return None

def find_pac_in_ticket_asn1(ticket: bytes) -> bytes | None:
    """
    Heuristic scan of raw ticket ASN.1 DER for the PAC payload.
    We look for the AD-WIN2K-PAC OID wrapper (type 0x0080 = 128 in the
    AD-IF-RELEVANT / AD-WIN2K-PAC structure).
    Strategy: scan for the 2-byte sequence 0x00 0x80 (big-endian int32 = 128)
    preceded by a 4-byte length that makes sense, then treat the remainder as
    the PAC buffer header.
    """
    # AD-WIN2K-PAC type encoded as 0x00000080 in the SEQUENCE
    marker = b'\x00\x00\x00\x80'
    idx = 0
    while True:
        idx = ticket.find(marker, idx)
        if idx == -1:
            break
        # The length of the AD value should follow as a 4-byte big-endian int
        data_start = idx + 4
        if data_start + 4 > len(ticket):
            idx += 1
            continue
        pac_len = struct.unpack(">I", ticket[data_start:data_start + 4])[0]
        pac_start = data_start + 4
        if pac_start + pac_len <= len(ticket) and pac_len > 16:
            candidate = ticket[pac_start:pac_start + pac_len]
            # Quick sanity check: first 4 bytes = number of PAC buffers (reasonable range)
            if len(candidate) >= 8:
                num_buffers = struct.unpack("<I", candidate[:4])[0]
                if 1 <= num_buffers <= 32:
                    return candidate
        idx += 1
    return None

# ── FILETIME helpers ──────────────────────────────────────────────────────────

EPOCH_DIFF = 116444736000000000  # 100ns ticks between 1601-01-01 and 1970-01-01

def filetime_to_dt(ft: int) -> str:
    """Convert a Windows FILETIME (100-ns intervals since 1601-01-01) to UTC string."""
    if ft == 0 or ft == 0x7FFFFFFFFFFFFFFF:
        return "Never / not set"
    try:
        ts = (ft - EPOCH_DIFF) / 1e7
        return datetime.datetime.fromtimestamp(ts, tz=datetime.timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC")
    except Exception:
        return f"0x{ft:016x}"

# ── RPC_UNICODE_STRING / NDR helpers ─────────────────────────────────────────

def read_rpc_unicode(buf: Buffer) -> str:
    """Read an RPC_UNICODE_STRING header (length, max_len, ptr)."""
    length  = buf.read_u16_le()
    max_len = buf.read_u16_le()
    _ptr    = buf.read_u32_le()   # pointer, resolved later
    return length  # actual string data comes later in the NDR stream

def decode_unicode_string(data: bytes, length: int) -> str:
    """Decode a UTF-16LE string of `length` bytes."""
    return data[:length].decode("utf-16-le", errors="replace")

def read_sid(buf: Buffer) -> str:
    """Parse a SID from binary representation."""
    revision        = buf.read_u8()
    sub_auth_count  = buf.read_u8()
    authority       = int.from_bytes(buf.read(6), "big")
    sub_authorities = [buf.read_u32_le() for _ in range(sub_auth_count)]
    rid = sub_authorities[-1] if sub_authorities else None
    sub_auth_str = "-".join(str(s) for s in sub_authorities)
    sid_str = f"S-{revision}-{authority}-{sub_auth_str}"
    return sid_str, rid

# ── LOGON_INFO (KerbValidationInfo) parser ────────────────────────────────────

def parse_logon_info(data: bytes) -> dict:
    """
    Parse the KERB_VALIDATION_INFO structure (PAC buffer type 1).
    This is an NDR-encoded structure; we do a best-effort sequential parse
    rather than a full NDR runtime.
    """
    result = {}
    buf = Buffer(data)

    try:
        # NDR common header (4 bytes) + private header (4 bytes)
        buf.read(8)

        # FILETIME LogonTime, LogoffTime, KickOffTime, PasswordLastSet,
        #          PasswordCanChange, PasswordMustChange
        result["logon_time"]          = filetime_to_dt(buf.read_u64_le())
        result["logoff_time"]         = filetime_to_dt(buf.read_u64_le())
        result["kickoff_time"]        = filetime_to_dt(buf.read_u64_le())
        result["pwd_last_set"]        = filetime_to_dt(buf.read_u64_le())
        result["pwd_can_change"]      = filetime_to_dt(buf.read_u64_le())
        result["pwd_must_change"]     = filetime_to_dt(buf.read_u64_le())

        # RPC_UNICODE_STRING headers: EffectiveName, FullName, LogonScript,
        #   ProfilePath, HomeDirectory, HomeDirectoryDrive
        name_len    = read_rpc_unicode(buf)   # EffectiveName
        full_len    = read_rpc_unicode(buf)   # FullName
        script_len  = read_rpc_unicode(buf)   # LogonScript
        profile_len = read_rpc_unicode(buf)   # ProfilePath
        home_len    = read_rpc_unicode(buf)   # HomeDirectory
        drive_len   = read_rpc_unicode(buf)   # HomeDirectoryDrive

        result["logon_count"]  = buf.read_u16_le()
        result["bad_pw_count"] = buf.read_u16_le()
        result["user_id"]      = buf.read_u32_le()   # RID
        result["primary_gid"]  = buf.read_u32_le()
        result["group_count"]  = buf.read_u32_le()
        _groups_ptr            = buf.read_u32_le()   # pointer to groups array
        result["user_flags"]   = buf.read_u32_le()

        # UserSessionKey (16 bytes)
        result["session_key"] = buf.read(16).hex()

        # RPC_UNICODE_STRING headers: LogonServer, LogonDomainName
        logon_server_len = read_rpc_unicode(buf)
        logon_domain_len = read_rpc_unicode(buf)

        _domain_id_ptr = buf.read_u32_le()  # pointer to LogonDomainId SID

        # LMOWFv1 (8 bytes), NTOWFv1 (8 bytes) – legacy, zeroed in modern tickets
        result["lm_owf"]  = buf.read(8).hex()
        result["nt_owf"]  = buf.read(8).hex()

        result["acct_flags"] = buf.read_u32_le()

        # SubAuthStatus, LastSuccessfulLogon, LastFailedLogon, FailedAttemptCount (optional)
        # We're now into referent data territory – resolve string pointers
        # by reading them sequentially as they appear in NDR wire format.

        strings = []
        # The NDR conformant strings follow: EffectiveName, FullName, LogonScript,
        # ProfilePath, HomeDirectory, HomeDirectoryDrive, LogonServer, LogonDomainName
        for expected_len in (name_len, full_len, script_len, profile_len,
                              home_len, drive_len, logon_server_len, logon_domain_len):
            if buf.remaining() < 12:
                strings.append("")
                continue
            # NDR conformant array: MaxCount, Offset, ActualCount
            max_count    = buf.read_u32_le()
            offset       = buf.read_u32_le()
            actual_count = buf.read_u32_le()
            raw = buf.read(actual_count * 2)
            strings.append(decode_unicode_string(raw, min(expected_len, len(raw))))

        labels = ["username", "full_name", "logon_script", "profile_path",
                  "home_dir", "home_drive", "logon_server", "logon_domain"]
        for label, value in zip(labels, strings):
            result[label] = value

        # Now read the groups array
        if buf.remaining() >= 4:
            count = buf.read_u32_le()  # conformant size
            groups = []
            for _ in range(count):
                if buf.remaining() < 8:
                    break
                rid        = buf.read_u32_le()
                attributes = buf.read_u32_le()
                groups.append((rid, attributes))
            result["groups"] = groups

        # Read LogonDomainId SID
        if buf.remaining() >= 8:
            try:
                sid_str, _ = read_sid(buf)
                result["domain_sid"] = sid_str
            except Exception:
                pass

        # Extra SIDs (optional, after main groups)
        if buf.remaining() >= 8:
            try:
                extra_count = buf.read_u32_le()
                _ptr        = buf.read_u32_le()
                extra_sids  = []
                if extra_count > 0 and buf.remaining() >= extra_count * 12:
                    for _ in range(extra_count):
                        _sid_ptr   = buf.read_u32_le()
                        attributes = buf.read_u32_le()
                        extra_sids.append(attributes)  # placeholder
                result["extra_sid_count"] = extra_count
            except Exception:
                pass

    except Exception as e:
        result["parse_error"] = str(e)

    return result

# ── UPN_DNS_INFO parser ───────────────────────────────────────────────────────

def parse_upn_dns_info(data: bytes) -> dict:
    """Parse UPN_DNS_INFO PAC buffer (type 10)."""
    result = {}
    buf = Buffer(data)
    try:
        upn_len       = buf.read_u16_le()
        upn_offset    = buf.read_u16_le()
        dns_len       = buf.read_u16_le()
        dns_offset    = buf.read_u16_le()
        flags         = buf.read_u32_le()
        result["flags"] = flags

        if upn_offset + upn_len <= len(data):
            result["upn"] = data[upn_offset:upn_offset + upn_len].decode("utf-16-le", errors="replace")
        if dns_offset + dns_len <= len(data):
            result["dns_domain"] = data[dns_offset:dns_offset + dns_len].decode("utf-16-le", errors="replace")
        # Extended fields (flags bit 3 = has_sam_name_and_sid)
        if flags & 0x4 and buf.remaining() >= 4:
            sam_len    = buf.read_u16_le()
            sam_offset = buf.read_u16_le()
            sid_len    = buf.read_u16_le()
            sid_offset = buf.read_u16_le()
            if sam_offset + sam_len <= len(data):
                result["sam_name"] = data[sam_offset:sam_offset + sam_len].decode("utf-16-le", errors="replace")
    except Exception as e:
        result["parse_error"] = str(e)
    return result

# ── CLIENT_INFO parser ────────────────────────────────────────────────────────

def parse_client_info(data: bytes) -> dict:
    """Parse PAC_CLIENT_INFO (type 6)."""
    result = {}
    buf = Buffer(data)
    try:
        client_id  = filetime_to_dt(buf.read_u64_le())
        name_len   = buf.read_u16_le()
        name_bytes = buf.read(name_len)
        result["client_id"]   = client_id
        result["client_name"] = name_bytes.decode("utf-16-le", errors="replace")
    except Exception as e:
        result["parse_error"] = str(e)
    return result

# ── PAC top-level parser ──────────────────────────────────────────────────────

def parse_pac(pac_data: bytes) -> list:
    """
    Parse the PAC binary blob.
    Returns a list of (type_name, buffer_type, parsed_data, raw_data).
    """
    buf = Buffer(pac_data)
    num_buffers = buf.read_u32_le()
    version     = buf.read_u32_le()

    buffers = []
    for _ in range(num_buffers):
        if buf.remaining() < 16:
            break
        buf_type   = buf.read_u32_le()
        buf_cbsize = buf.read_u32_le()   # size in bytes
        buf_offset = buf.read_u64_le()   # offset from start of PAC

        type_name  = PAC_BUFFER_TYPES.get(buf_type, f"UNKNOWN ({buf_type})")
        raw        = pac_data[buf_offset:buf_offset + buf_cbsize]

        parsed = None
        if buf_type == 1:   # LOGON_INFO
            parsed = parse_logon_info(raw)
        elif buf_type == 6: # CLIENT_INFO
            parsed = parse_client_info(raw)
        elif buf_type == 10: # UPN_DNS_INFO
            parsed = parse_upn_dns_info(raw)
        elif buf_type in (3, 4, 14):  # checksum buffers
            parsed = {"raw": raw.hex()}

        buffers.append((type_name, buf_type, parsed, raw))

    return buffers

